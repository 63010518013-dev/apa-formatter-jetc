import { NextResponse } from 'next/server';
import { GoogleGenerativeAI } from '@google/generative-ai';

const apiKey = process.env.GEMINI_API_KEY;
const genAI = new GoogleGenerativeAI(apiKey || '');

export async function POST(req: Request) {
  if (!apiKey) {
    return NextResponse.json({ error: 'ไม่พบ API Key' }, { status: 500 });
  }

  try {
    const { text } = await req.json();

    if (!text) {
      return NextResponse.json({ error: 'กรุณาส่งข้อความที่ต้องการตรวจสอบ' }, { status: 400 });
    }

    // เปลี่ยนมาใช้ gemini-2.5-flash ซึ่งเป็นเวอร์ชันใหม่ล่าสุด
const model = genAI.getGenerativeModel({ model: 'gemini-2.5-flash' });

const prompt = `
คุณคือผู้เชี่ยวชาญระดับสูงด้านการตรวจสอบและจัดรูปแบบบรรณานุกรมตามหลัก APA 7th Edition (ภาษาไทยและภาษาอังกฤษ)
จงจัดรูปแบบข้อความบรรณานุกรมต่อไปนี้ให้ถูกต้อง 100% ตามกฎเกณฑ์ที่กำหนดอย่างเคร่งครัด:

**กฎพื้นฐาน (Core Rules):**
1. ลำดับ: แยกรายการภาษาไทยขึ้นก่อนภาษาอังกฤษ และเรียงลำดับตามตัวอักษร (ก-ฮ และ A-Z)
2. การใช้ตัวเอียง: 
   - หนังสือ, รายงาน, วิทยานิพนธ์, Proceeding (เล่ม): พิมพ์ "ชื่อเรื่อง" เป็นตัวเอียง
   - วารสาร: พิมพ์ "ชื่อวารสาร" และ "เลขของปีที่ (Volume)" เป็นตัวเอียง (ชื่อบทความไม่ต้องเอียง)
3. สถานที่พิมพ์: ❌ ห้ามใส่เมือง/ประเทศ ให้ใส่เฉพาะ "สำนักพิมพ์" เท่านั้น (เช่น ตัด กรุงเทพฯ: ทิ้ง)
4. ผู้แต่งหลายคน: ❌ ห้ามใช้คำว่า "และคณะ" ในรายการอ้างอิงเด็ดขาด ต้องใส่ชื่อให้ครบ
5. การเชื่อมผู้แต่ง: คนสุดท้ายให้ใช้คำว่า "และ" (ไทย) หรือ "&" (อังกฤษ) **โดยห้ามเว้นวรรคหลังคำว่า "และ"** (เช่น "สมชาย, และสมหมาย" ❌ห้ามพิมพ์ "และ สมหมาย")
6. ชื่อภาษาไทย: ให้คงรูปแบบ "ชื่อ นามสกุล" เต็มไว้ ❌ห้ามย่อชื่อต้นแบบภาษาอังกฤษ
7. เครื่องหมายวรรคตอน: ตรวจสอบจุด (.) ลูกน้ำ (,) และเว้นวรรค 1 เคาะหลังเครื่องหมายให้ถูกต้อง 100%
8. ปีที่พิมพ์: ต้องอยู่ในวงเล็บเสมอ เช่น (2566). หากไม่มีให้ใส่ (ม.ป.ป.) หรือ (n.d.)

**กฎเฉพาะประเภทเอกสาร:**
- วิทยานิพนธ์: ต้องระบุประเภทในวงเล็บเหลี่ยม เช่น [วิทยานิพนธ์ปริญญามหาบัณฑิต ไม่ได้ตีพิมพ์] หรือ [Doctoral dissertation, University Name] ❌ห้ามใช้วงเล็บกลม
- หนังสือแปล: ชื่อผู้แต่ง. (ปี). ชื่อต้นฉบับ [ชื่อแปลไทย]. สำนักพิมพ์.
- ครั้งที่พิมพ์: กรณีพิมพ์ครั้งที่ 1 ไม่ต้องระบุ ถ้าพิมพ์ครั้งอื่นให้ระบุในวงเล็บ เช่น (พิมพ์ครั้งที่ 3) หรือ (2 ed.)
- แหล่งออนไลน์/วารสาร: ต้องมี URL หรือ DOI หากรายการใดเป็นแหล่งออนไลน์แต่ผู้ใช้ไม่ได้ใส่ลิงก์มา ให้เติมข้อความ ⚠️ [กรุณาเพิ่มลิงก์ URL หรือ DOI] ต่อท้ายรายการนั้น

**รูปแบบผลลัพธ์ที่ต้องการ (บังคับส่งกลับมา 2 ส่วน คั่นด้วย ||| เท่านั้น ❌ห้ามพิมพ์คำว่า "ส่วนที่ 1" หรือ "ส่วนที่ 2" หรือใส่วงเล็บเหลี่ยมครอบหัวข้อมาเด็ดขาด):**
(พิมพ์เฉพาะรายการบรรณานุกรมที่จัดรูปแบบแล้วตรงนี้)
|||
(พิมพ์เฉพาะสรุปสิ่งที่แก้ไขเป็นข้อๆ ตรงนี้)

ข้อความต้นฉบับ:
${text}
    `;

    const result = await model.generateContent(prompt);
    const responseText = result.response.text();

    return NextResponse.json({ result: responseText });

  } catch (error: any) {
    console.error('Gemini API Error:', error);
    // ส่ง Error Message จริงๆ ไปแสดงผลที่หน้าจอ จะได้แก้ปัญหาได้ตรงจุด
    return NextResponse.json({ error: `เกิดข้อผิดพลาด: ${error.message}` }, { status: 500 });
  }
}